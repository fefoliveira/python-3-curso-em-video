def soma(a, b, c=0):
    """
    -> Faz a soma de três valores e mostra o resultado na tela.
    :param a: primeira parcela
    :param b: segunda parcela
    :param c: terceira parcela
    :return: sem retorno
    Função criada por Fernando de Souza Oliveira, aluno do Curso em Vídeo.
    """
    print(a + b + c)


